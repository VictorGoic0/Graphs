    def bft(self, starting_vertex):
        queue = Queue()
        queue.enqueue(starting_vertex)
        visited = set()
        path = []
        while queue.size() > 0:
            node = queue.dequeue()
            if node not in visited:
                visited.add(node)
                path.append(node)
                for neighbor in self.vertices[node]:
                    queue.enqueue(neighbor)
        print(path)
